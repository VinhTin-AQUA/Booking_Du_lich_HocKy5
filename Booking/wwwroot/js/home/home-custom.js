// hàm sự kiện
function onSelectCity(x) {
    let cityItem = document.querySelectorAll('.city-item');



    for (let item of cityItem) {
        item.classList.remove('active-city-item');
    }


    let cityItemActive = document.getElementById(x);
    cityItemActive.classList.add('active-city-item');

    if (x === "hcm") {
        generateElements(hcm)

    } else if (x === "hn") {
        generateElements(hn)

    } else if (x === "vt") {
        generateElements(vt)

    } else if (x === "dn") {
        generateElements(dn)

    } else if (x === "dl") {
        generateElements(dl)

    }
}

function generateElements(array) {
    let cityItemsRef = document.getElementById("city-items");
    cityItemsRef.innerHTML = "";


    for (let item of array) {
        // Tạo thẻ div với class "col-xxl-3 col-md-4 col-sm-6 col-12"
        const outerDiv = document.createElement("div");
        outerDiv.classList.add("col-xxl-3", "col-md-4", "col-sm-6", "col-12");

        // Tạo thẻ div với class "product-card"
        const productCardDiv = document.createElement("div");
        productCardDiv.classList.add("product-card");

        // Tạo thẻ img với class "product-card-img-top" và thuộc tính src
        const img = document.createElement("img");
        img.classList.add("product-card-img-top");
        img.style.maxWidth = '296px';
        img.style.maxHeight = '190px';
        img.style.minWidth = '296px';
        img.style.minHeight = '190px';

        img.src = item.imgUrl;
        img.alt = "Bootstrap Gallery";

        // Tạo thẻ div với class "product-card-body"
        const productCardBodyDiv = document.createElement("div");
        productCardBodyDiv.classList.add("product-card-body");

        // Tạo thẻ h5 với class "product-title" và nội dung "Green Salad"
        const h5 = document.createElement("h5");
        h5.classList.add("product-title");
        h5.textContent = item.tourName;

        // Tạo thẻ div với class "product-price"
        const productPriceDiv = document.createElement("div");
        productPriceDiv.classList.add("product-price");

        // Tạo thẻ span với class "disount-price" và nội dung "VNĐ 20"
        const span = document.createElement("span");
        span.classList.add("disount-price");
        span.textContent = "VNĐ " + item.price;

        // Tạo thẻ div với class "product-description" và nội dung
        const productDescriptionDiv = document.createElement("div");
        productDescriptionDiv.classList.add("product-description");
        productDescriptionDiv.textContent = item.location;

        // Tạo thẻ div với class "product-actions"
        const productActionsDiv = document.createElement("div");
        productActionsDiv.classList.add("product-actions");

        // Tạo thẻ button với class "btn btn-success addToCart" và nội dung "Xem"
        const button = document.createElement("button");
        button.classList.add("btn", "btn-success", "addToCart");
        button.textContent = "Xem";

        // Gắn các thẻ con vào các thẻ cha theo cấu trúc
        productPriceDiv.appendChild(span);
        productCardBodyDiv.appendChild(h5);
        productCardBodyDiv.appendChild(productPriceDiv);
        productCardBodyDiv.appendChild(productDescriptionDiv);
        productCardBodyDiv.appendChild(productActionsDiv);
        productActionsDiv.appendChild(button);
        productCardDiv.appendChild(img);
        productCardDiv.appendChild(productCardBodyDiv);
        outerDiv.appendChild(productCardDiv);

        // Đưa toàn bộ cấu trúc vào body của trang HTML
        cityItemsRef.appendChild(outerDiv);
    }
}
